어제 저희 21번째 여행 후기입니다!
2015년 1월 10일 토요일 AGO 미켈란젤로 습작 특별전이 끝나기 하루전날 AGO에 다녀왔습니다. 체감온도 -20도에달하는 추운 날씨였지만 많은 사람들과 함께 다녀와서 정말 재미있었습니다! 버스가 딜레이되고 그러는 바람에 약속했던 시간보다 15분, 30분 가량이 지나서야 AGO 안에서 모였는데요, coat check를 하고 학생증제시후 $16.50에 티켓을 사고 AGO로 입장을 하였습니다! 저와 Heejae Lucy Choi, Catherine Lee, Ki Myung Song, Haseong Kim, Anthony Kim, Yoo-Jin Kim, Gowan Kim 8명이 모여서 인사를 나눈뒤 희재의 AGO에 관한 설명을 짧게 듣고, 미켈란젤로 특별전에 입장을 하였습니다. 역시 전시 마지막에다보니 관람을 온 사람들이 정말 많았는데요, 그래도 사람들이 다 여유가 있어서 그런지천천히 읽고 보며 여유롭게 관람을 할 수 있었습니다. 유명한 천재 조각가로만 알고있던 미켈란젤로의 습작들을 보다보니, 이 사람도 역시 자신의 작품의 많은 시간과 노력을 쏟아부었던 노력파였다는것을 느꼈는데요, 특히 세세한 근육이나 생동감을 표현하려 여러자세를 그려보고 고민했다는 점이 참 신선했습니다. 개인적으로 제일 기억에 남는건 역시 근육이였습니다.. 아기의 허벅지 근육과 이두박근을 보고 있자니 이번 2015년 운동을 다시 열심히 해보자라는 의지가 생겼습니다. 인체뿐만 아니라 건축에도 쏟은 열정이 많았다는것을 볼 수 있었습니다. 미켈란젤로가 그렸던 여러가지 설계도면을 보자니 지금의 그것들과 많이 달라보이지 않아서 역시 시간을 뛰어넘는 사람이였구나를 느꼈어요!
30분정도면 다 볼 수 있을거라는 예상과 달리 모두 여유롭게 관람하고 나니 1시간 30분정도가 걸렸는데요, 잠시 AGO내의 카페에가서 이야기도 나누고 하다가 4, 5층 contemporary art를 보러 갔습니다! 올라가는 나선형 계단 모양이 독특해서 4층까지 계단으로 올라가는데.. 보기보다 계단 수가 많았어요.. 헥헥거리면서 4층에 올라와서 기명이형의 art performance도 보고 이해할 수 없는 예술의 세계를 새삼 느끼며 AGO를 나왔습니다.
